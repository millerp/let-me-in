<?php

namespace Squid\LetMeIn\Calculator;

class Calculator {

    public function sum($x, $y) {
        return $x + $y;
    }

    public function subtract($x, $y) {
        return $x - $y;
    }

    public function multiply($x, $y) {
        return $x * $y;
    }

    public function divide($x, $y) {
        return $x / $y;
    }

}
